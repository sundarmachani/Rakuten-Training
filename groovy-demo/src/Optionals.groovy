
class Optionals {
	static void main(String[] args) {
		// Example of an Integer using def
		def a = 100;
		println(a);
		  
		// Example of an float using def
		def b = 100.10;
		println(b);
		  
		// Example of an Double using def
		def c = 100.101;
		println(c);
		  
		// Example of an String using def
		def d = "HelloWorld";
		println(d);
	 }
}
